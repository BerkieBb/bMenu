CreateThread(function()
    while true do
        Wait(0)
        local coords
        if showCoords then
            coords = not coords and GetEntityCoords(cache.ped) or coords
            SetScriptGfxAlign(0, 84)
            SetScriptGfxAlignParams(0, 0, 0, 0)
            local screenWidth = GetActiveScreenResolution()
            DrawTextOnScreen(('~r~X~s~ \t\t%s\n~r~Y~s~ \t\t%s\n~r~Z~s~ \t\t%s\n~r~Heading~s~ \t%s'):format(coords.x, coords.y, coords.z, GetEntityHeading(cache.ped)), 0.5 - (30 / screenWidth), 0, 0.5, 1, 6, false)
            ResetScriptGfxAlign()
        end

        if showVehicleDimensions then
            local vehiclePool = GetGamePool('CVehicle')
            coords = not coords and GetEntityCoords(cache.ped) or coords
            for i = 1, #vehiclePool do
                local vehicle = vehiclePool[i]
                local position = GetEntityCoords(vehicle)
                if IsEntityOnScreen(vehicle) and #(coords - position) < dimensionsRadius then
                    drawEntityBoundingBox(vehicle, 250, 150, 0, 100)

                    if showEntityHandles then
                        SetDrawOrigin(position.x, position.y, position.z, 0)
                        DrawTextOnScreen(('Vehicle Handle: %s'):format(vehicle), 0, 0, 0.3, 0, 0, false)
                        ClearDrawOrigin()
                    end

                    if showEntityModels then
                        SetDrawOrigin(position.x, position.y, position.z - 0.3, 0)
                        local model = GetEntityModel(vehicle)
                        DrawTextOnScreen(('Vehicle Model: %s / %s / 0x%x'):format(GetEntityArchetypeName(vehicle), model, model), 0, 0, 0.3, 0, 0, false)
                        ClearDrawOrigin()
                    end

                    if showEntityNetworkOwners then
                        local networkOwner = NetworkGetEntityOwner(vehicle)
                        if networkOwner ~= 0 then
                            SetDrawOrigin(position.x, position.y, position.z + 0.3, 0)
                            DrawTextOnScreen(('Owner ID: %s | %s'):format(GetPlayerServerId(networkOwner), GetPlayerName(networkOwner)), 0, 0, 0.3, 0, 0, false)
                            ClearDrawOrigin()
                        end
                    end
                end
            end
        end

        if showPropDimensions then
            local objectPool = GetGamePool('CObject')
            coords = not coords and GetEntityCoords(cache.ped) or coords
            for i = 1, #objectPool do
                local object = objectPool[i]
                local position = GetEntityCoords(object)
                if IsEntityOnScreen(object) and #(coords - position) < dimensionsRadius then
                    drawEntityBoundingBox(object, 255, 0, 0, 100)

                    if showEntityHandles then
                        SetDrawOrigin(position.x, position.y, position.z, 0)
                        DrawTextOnScreen(('Prop Handle: %s'):format(object), 0, 0, 0.3, 0, 0, false)
                        ClearDrawOrigin()
                    end

                    if showEntityModels then
                        SetDrawOrigin(position.x, position.y, position.z - 0.3, 0)
                        local model = GetEntityModel(object)
                        DrawTextOnScreen(('Prop Model: %s / %s / 0x%x'):format(GetEntityArchetypeName(object), model, model), 0, 0, 0.3, 0, 0, false)
                        ClearDrawOrigin()
                    end

                    if showEntityNetworkOwners then
                        local networkOwner = NetworkGetEntityOwner(object)
                        if networkOwner ~= 0 then
                            SetDrawOrigin(position.x, position.y, position.z + 0.3, 0)
                            DrawTextOnScreen(('Owner ID: %s | %s'):format(GetPlayerServerId(networkOwner), GetPlayerName(networkOwner)), 0, 0, 0.3, 0, 0, false)
                            ClearDrawOrigin()
                        end
                    end
                end
            end
        end

        if showPedDimensions then
            local pedPool = GetGamePool('CPed')
            coords = not coords and GetEntityCoords(cache.ped) or coords
            for i = 1, #pedPool do
                local ped = pedPool[i]
                local position = GetEntityCoords(ped)
                if IsEntityOnScreen(ped) and #(coords - position) < dimensionsRadius then
                    drawEntityBoundingBox(ped, 50, 255, 50, 100)

                    if showEntityHandles then
                        SetDrawOrigin(position.x, position.y, position.z, 0)
                        DrawTextOnScreen(('Ped Handle: %s'):format(ped), 0, 0, 0.3, 0, 0, false)
                        ClearDrawOrigin()
                    end

                    if showEntityModels then
                        SetDrawOrigin(position.x, position.y, position.z - 0.3, 0)
                        local model = GetEntityModel(ped)
                        DrawTextOnScreen(('Ped Model: %s / %s / 0x%x'):format(GetEntityArchetypeName(ped), model, model), 0, 0, 0.3, 0, 0, false)
