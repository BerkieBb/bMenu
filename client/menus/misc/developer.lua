                            SetDrawOrigin(position.x, position.y, position.z + 0.3, 0)
                            DrawTextOnScreen(('Owner ID: %s | %s'):format(GetPlayerServerId(networkOwner), GetPlayerName(networkOwner)), 0, 0, 0.3, 0, 0, false)
                            ClearDrawOrigin()
