CreateThread(function()
    local headingOffset = 0
    while true do
        Wait(0)
        if spawningActive and currentEntity ~= 0 then
            if curScaleform == -1 then
                curScaleform = RequestScaleformMovie('INSTRUCTIONAL_BUTTONS')
                while not HasScaleformMovieLoaded(curScaleform) do
                    Wait(100)
                end

                BeginScaleformMovieMethod(curScaleform, 'CLEAR_ALL')
                EndScaleformMovieMethod()

                BeginScaleformMovieMethod(curScaleform, 'SET_DATA_SLOT')
                ScaleformMovieMethodAddParamInt(0)
                PushScaleformMovieMethodParameterString('~INPUT_VEH_FLY_ROLL_LR~')
                PushScaleformMovieMethodParameterString('Rotate Object')
                EndScaleformMovieMethod()

                BeginScaleformMovieMethod(curScaleform, 'DRAW_INSTRUCTIONAL_BUTTONS')
                ScaleformMovieMethodAddParamInt(0)
                EndScaleformMovieMethod()
            end

            DrawScaleformMovieFullscreen(curScaleform, 255, 255, 255, 255, 0)

            FreezeEntityPosition(currentEntity, true)
            SetEntityInvincible(currentEntity, true)
            SetEntityCollision(currentEntity, false, false)
            SetEntityAlpha(currentEntity, 102, false)
            SetEntityHeading(currentEntity, (GetGameplayCamRot(0).z + headingOffset) % 360)

            -- Borrowed from ox_target (https://github.com/overextended/ox_target/blob/main/client/utils.lua)
            -- This doesn't work yet somehow, so please fix if you get to it or I will do it either way, problem is it keeps not hitting anything with the shapetest
            local coords, normal = GetWorldCoordFromScreenCoord(0.5, 0.5)
            local destination = coords + normal * 10
            local handle = StartShapeTestLosProbe(coords.x, coords.y, coords.z, destination.x, destination.y, destination.z, 511 --[[511 means include everything]], cache.ped, 4)
            local _, _, newPosition = GetShapeTestResultIncludingMaterial(handle)

            SetEntityCoords(currentEntity, newPosition.x, newPosition.y, newPosition.z, false, false, false, false)

            if GetEntityHeightAboveGround(currentEntity) < 3 then
                PlaceObjectOnGroundProperly(currentEntity)
            end

            if IsControlPressed(0, 108) then
                headingOffset += rotateSpeed * GetFrameTime()
            elseif IsControlPressed(0, 109) then
                headingOffset -= rotateSpeed * GetFrameTime()
            end

            FreezeEntityPosition(currentEntity, false)
            SetEntityInvincible(currentEntity, false)
            SetEntityCollision(currentEntity, true, true)
            ResetEntityAlpha(currentEntity)
        elseif curScaleform ~= -1 then
            SetScaleformMovieAsNoLongerNeeded(curScaleform)
            curScaleform = -1
        end
    end
end)

