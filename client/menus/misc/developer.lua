local function DrawTextOnScreen(text, x, y, size, position --[[ 0: center | 1: left | 2: right ]], font, disableTextOutline)
    if
    not IsHudPreferenceSwitchedOn()
    or IsHudHidden()
    or IsPlayerSwitchInProgress()
    or IsScreenFadedOut()
    or IsPauseMenuActive()
    or IsFrontendFading()
    or IsPauseMenuRestarting()
    then
        return
    end

    size = size or 0.48
    position = position or 1
    font = font or 6

    SetTextFont(font)
    SetTextScale(1.0, size)
    if position == 2 then
        SetTextWrap(0, x)
    end
    SetTextJustification(position)
    if not disableTextOutline then
        SetTextOutline()
    end
    BeginTextCommandDisplayText('STRING')
    AddTextComponentSubstringPlayerName(text)
    EndTextCommandDisplayText(x, y)
end

local function drawEntityBoundingBox(entity, r, g, b, alpha)
    local pad = 0.001
    local min, max = GetModelDimensions(GetEntityModel(entity))
    local boundingBox = {
        -- Bottom
        GetOffsetFromEntityInWorldCoords(entity, min.x - pad, min.y - pad, min.z - pad),
        GetOffsetFromEntityInWorldCoords(entity, max.x + pad, min.y - pad, min.z - pad),
        GetOffsetFromEntityInWorldCoords(entity, max.x + pad, max.y + pad, min.z - pad),
        GetOffsetFromEntityInWorldCoords(entity, min.x - pad, max.y + pad, min.z - pad),

        -- Top
        GetOffsetFromEntityInWorldCoords(entity, min.x - pad, min.y - pad, max.z + pad),
        GetOffsetFromEntityInWorldCoords(entity, max.x + pad, min.y - pad, max.z + pad),
        GetOffsetFromEntityInWorldCoords(entity, max.x + pad, max.y + pad, max.z + pad),
        GetOffsetFromEntityInWorldCoords(entity, min.x - pad, max.y + pad, max.z + pad)
    }

    local polyMatrix = {
        {boundingBox[3], boundingBox[2], boundingBox[1]},
        {boundingBox[4], boundingBox[3], boundingBox[1]},

        {boundingBox[5], boundingBox[6], boundingBox[7]},
        {boundingBox[5], boundingBox[7], boundingBox[8]},

        {boundingBox[3], boundingBox[4], boundingBox[7]},
        {boundingBox[8], boundingBox[7], boundingBox[4]},

        {boundingBox[1], boundingBox[2], boundingBox[5]},
        {boundingBox[6], boundingBox[5], boundingBox[2]},

        {boundingBox[2], boundingBox[3], boundingBox[6]},
        {boundingBox[3], boundingBox[7], boundingBox[6]},

        {boundingBox[5], boundingBox[8], boundingBox[4]},
        {boundingBox[5], boundingBox[4], boundingBox[1]}
    }

    local edgeMatrix = {
        {boundingBox[1], boundingBox[2]},
        {boundingBox[2], boundingBox[3]},
        {boundingBox[3], boundingBox[4]},
        {boundingBox[4], boundingBox[1]},

        {boundingBox[5], boundingBox[6]},
        {boundingBox[6], boundingBox[7]},
        {boundingBox[7], boundingBox[8]},
        {boundingBox[8], boundingBox[5]},

        {boundingBox[1], boundingBox[5]},
        {boundingBox[2], boundingBox[6]},
        {boundingBox[3], boundingBox[7]},
        {boundingBox[4], boundingBox[8]}
    }

    for i = 1, #polyMatrix do
        local data = polyMatrix[i]
        DrawPoly(data[1].x, data[1].y, data[1].z, data[2].x, data[2].y, data[2].z, data[3].x, data[3].y, data[3].z, r, g, b, alpha)
    end

    for i = 1, #edgeMatrix do
        local data = edgeMatrix[i]
        DrawLine(data[1].x, data[1].y, data[1].z, data[2].x, data[2].y, data[2].z, 255, 255, 255, 255)
    end
end

local function createSpawnerMenu()
    lib.registerMenu({
        id = 'bMenu_misc_options_developer_options_spawner',
        title = 'Entity Spawner',
        position = MenuPosition,
        onSelected = function(selected)
            MenuIndexes['bMenu_misc_options_developer_options_spawner'] = selected
        end,
        onClose = function(keyPressed)
            if DoesEntityExist(currentEntity) then
                DeleteEntity(currentEntity)
                currentEntity = 0
            end
            CloseMenu(false, keyPressed, 'bMenu_misc_options_developer_options')
        end,
        options = {
            {label = 'Spawn New Entity', description = 'Spawns entity into the world and lets you set its position and rotation', args = {'spawn_new_entity'}},
            {label = 'Confirm Entity Position', description = 'Stops placing entity and sets it at it current location', args = {'confirm_entity_position'}, close = false},
            {label = 'Confirm Entity Position And Duplicate', description = 'Stops placing entity and sets it at it current location and creates new one to place', args = {'confirm_duplicate'}, close = false},
            {label = 'Cancel', description = 'Deletes current entity and cancels its placement', args = {'cancel'}, close = false}
        }
    }, function(_, _, args)
        if args[1] == 'spawn_new_entity' then
            if DoesEntityExist(currentEntity) then
                lib.notify({
                    description = 'You\'re already spawning an entity, cancel it or confirm it first',
                    type = 'error'
                })
                return
            end

            local dialog = lib.inputDialog('Spawn New Entity', {'Entity Name'})

            if not dialog or not dialog[1] or dialog[1] == '' then
                return
            end

            local model = joaat(dialog[1])

            if not IsModelValid(model) then
                lib.notify({
                    description = ('Model %s doesn\'t exist'):format(dialog[1]),
                    type = 'error'
                })
                return
            end

            lib.requestModel(model)

            local coords = GetEntityCoords(cache.ped)
            currentEntity = CreatePed(4, model, coords.x, coords.y, coords.z, GetEntityHeading(cache.ped), true, true)
            SetEntityAsMissionEntity(currentEntity, true, true)
            spawningActive = true
        elseif args[1] == 'confirm_entity_position' or args[1] == 'confirm_duplicate' then
            local duplicate = args[1] == 'confirm_duplicate'
            if duplicate and DoesEntityExist(currentEntity) then
                local coords = GetEntityCoords(currentEntity)
                local heading = GetEntityHeading(currentEntity)
                local model = GetEntityModel(currentEntity)
                spawningActive = false
                Wait(1)
                currentEntity = CreatePed(4, model, coords.x, coords.y, coords.z, heading, true, true)
                SetEntityAsMissionEntity(currentEntity, true, true)
                spawningActive = true
            else
                spawningActive = false
                Wait(1)
                currentEntity = 0
            end
        end
    end)
end

